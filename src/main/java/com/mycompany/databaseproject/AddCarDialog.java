    private boolean plateExists(String plate) {
        String sql = "SELECT COUNT(*) FROM Car WHERE PlateNumber = ?";
        try (ResultSet rs = DatabaseHelper.executeQuery(sql, plate)) {
            if (rs.next()) {
                return rs.getInt(1) > 0; // true if exists
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return false;
    }

